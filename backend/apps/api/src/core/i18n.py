def normalize_locale(locale: str | None) -> str:
    """将 cookie/header locale 收敛到两个受支持值。"""
    if not locale:
        return DEFAULT_LOCALE
    value = locale.strip().replace("_", "-").lower()
    if value == "en" or value.startswith("en-"):
        return "en-HK"
    if value in {"zh-hk", "zh-hant", "zh-hant-hk"}:
        return "zh-HK"
    return DEFAULT_LOCALE


def locale_from_request(cookie_locale: str | None, accept_language: str | None) -> str:
    """cookie 优先；无有效 cookie 时按 Accept-Language 的 q 值选择。"""
    if cookie_locale and normalize_locale(cookie_locale) in SUPPORTED_LOCALES:
        normalized_cookie = cookie_locale.strip().replace("_", "-").lower()
        if normalized_cookie == "en-hk" or normalized_cookie == "zh-hk":
            return normalize_locale(cookie_locale)

    choices: list[tuple[float, int, str]] = []
    for index, part in enumerate((accept_language or "").split(",")):
        bits = [item.strip() for item in part.split(";")]
        language = bits[0]
        quality = 1.0
        for parameter in bits[1:]:
            if parameter.startswith("q="):
                try:
                    quality = float(parameter[2:])
                except ValueError:
                    quality = 0.0
        if quality <= 0:
            continue
        lowered = language.replace("_", "-").lower()
        if lowered == "en" or lowered.startswith("en-"):
            choices.append((quality, -index, "en-HK"))
        elif lowered == "zh" or lowered.startswith("zh-"):
            choices.append((quality, -index, "zh-HK"))
    return max(choices)[2] if choices else DEFAULT_LOCALE


def get_locale() -> str:
    return _request_locale.get()


def set_locale(locale: str) -> Token[str]:
    return _request_locale.set(normalize_locale(locale))


def reset_locale(token: Token[str]) -> None:
    _request_locale.reset(token)


def translate_message(
    source: str | None,
    locale: str | None = None,
    params: dict[str, Any] | None = None,
) -> str | None:
    """翻译稳定 key 或原始消息；未知文本原样返回，避免损失业务细节。"""
    if source is None:
        return None
    target_locale = normalize_locale(locale) if locale else get_locale()
    key = source if source in _MESSAGES["zh-HK"] else _SOURCE_KEYS.get(source)
    if key:
        template = _MESSAGES[target_locale].get(key) or _MESSAGES[DEFAULT_LOCALE].get(key)
        if template:
            try:
                return template.format(**(params or {}))
            except (KeyError, ValueError):
                return template

    for pattern, zh_template, en_template in _DYNAMIC_PATTERNS:
        match = pattern.fullmatch(source)
        if match:
            values = {**match.groupdict(), **(params or {})}
            if "detail" in values:
                values["detail"] = translate_message(str(values["detail"]), target_locale)
            template = en_template if target_locale == "en-HK" else zh_template
            return template.format(**values)
    return source
