#!/usr/bin/env node
// Generate the developer-readable API reference from the canonical contract.
//
// Input:  packages/types/openapi.json (single source of truth)
// Output: docs/api/reference.md — operations grouped by tag with parameters,
//         request/response schemas, and backend-status markers, followed by a
//         schema dictionary for every component.
//
// Zero-runtime by design: plain Node, no dependencies, committed output.
// Re-run after any spec change:  pnpm run gen:api-docs
//
// Hand-authored companions in docs/api/: implementation-notes.md (intentional
// deviations + Phase-1 deltas) and endpoint-checklist.md (generated by
// packages/api-client/scripts/gen-endpoint-checklist.mjs).

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO = resolve(HERE, "..");
const SPEC = resolve(REPO, "packages/types/openapi.json");
const OUT = resolve(REPO, "docs/api/reference.md");

const METHODS = ["get", "post", "put", "patch", "delete"];

const spec = JSON.parse(readFileSync(SPEC, "utf8"));
const schemas = spec.components?.schemas ?? {};

// --- schema rendering helpers ------------------------------------------------

function refName(ref) {
  return ref.split("/").pop();
}

function anchor(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, "");
}

// One-line type expression for a schema node (links component refs).
function typeExpr(node) {
  if (!node) return "—";
  if (node.$ref) {
    const n = refName(node.$ref);
    return `[${n}](#${anchor(n)})`;
  }
  if (node.anyOf) return node.anyOf.map(typeExpr).join(" \\| ");
  if (node.allOf) return node.allOf.map(typeExpr).join(" & ");
  if (node.enum) return node.enum.map((v) => `\`${JSON.stringify(v)}\``).join(" \\| ");
  if (node.type === "array") return `array of ${typeExpr(node.items)}`;
  if (node.type === "object") {
    if (node.additionalProperties && !node.properties) {
      return `object of ${typeExpr(node.additionalProperties)}`;
    }
    return node.title ? `object (${node.title})` : "object";
  }
  if (node.type === "null") return "null";
  if (node.type) return node.type + (node.format ? ` (${node.format})` : "");
  return "any";
}

function schemaPropertiesTable(schema, indentNote = "") {
  // allOf composition: base refs listed, own properties from inline members.
  let md = "";
  if (schema.allOf) {
    const bases = schema.allOf.filter((m) => m.$ref);
    const inline = schema.allOf.filter((m) => !m.$ref);
    if (bases.length > 0) {
      md += `Extends ${bases.map(typeExpr).join(", ")}.\n\n`;
    }
    for (const member of inline) md += schemaPropertiesTable(member, indentNote);
    return md;
  }
  if (schema.enum) {
    md += `Enum: ${schema.enum.map((v) => `\`${v}\``).join(", ")}\n\n`;
    return md;
  }
  const props = schema.properties;
  if (!props) {
    md += `Type: ${typeExpr(schema)}\n\n`;
    return md;
  }
  const required = new Set(schema.required ?? []);
  md += "| Field | Type | Required | Notes |\n|---|---|---|---|\n";
  for (const [name, node] of Object.entries(props)) {
    const notes = [];
    if (node.deprecated) notes.push("**Deprecated.**");
    if (node.description) notes.push(node.description);
    if (node.default !== undefined) notes.push(`Default: \`${JSON.stringify(node.default)}\`.`);
    md += `| \`${name}\` | ${typeExpr(node)} | ${required.has(name) ? "yes" : "no"} | ${notes.join(" ").replaceAll("|", "\\|").replaceAll("\n", " ")} |\n`;
  }
  md += "\n";
  return md;
}

// --- operations rendering ------------------------------------------------------

function opAuth(op) {
  const sec = op.security ?? spec.security;
  if (!sec || sec.length === 0 || (Array.isArray(sec) && sec.every((s) => Object.keys(s).length === 0))) {
    return "none";
  }
  if (op.security === undefined) return "bearer / cookie";
  return op.security.length === 0 ? "none" : "bearer / cookie";
}

function renderOp(path, method, op) {
  let md = "";
  md += `### ${method.toUpperCase()} \`${path}\`\n\n`;
  const status = op["x-backend-status"];
  const meta = [];
  if (op.summary) meta.push(op.summary);
  if (status) meta.push(`**Backend status: ${status}** (forward contract — not yet implemented by the backend)`);
  if (meta.length) md += `${meta.join(" — ")}\n\n`;
  if (op.description) md += `${op.description}\n\n`;
  if (op["x-implementation-note"]) md += `> ${op["x-implementation-note"]}\n\n`;

  md += `Auth: ${opAuth(op)}\n\n`;

  const params = (op.parameters ?? []).map((p) =>
    p.$ref ? spec.components.parameters[refName(p.$ref)] : p,
  );
  const pathParams = params.filter((p) => p.in === "path");
  const queryParams = params.filter((p) => p.in === "query");
  const headerParams = params.filter((p) => p.in === "header");
  const renderParams = (list, label) => {
    if (list.length === 0) return "";
    let s = `${label}:\n\n| Name | Type | Required | Notes |\n|---|---|---|---|\n`;
    for (const p of list) {
      s += `| \`${p.name}\` | ${typeExpr(p.schema)} | ${p.required ? "yes" : "no"} | ${(p.description ?? "").replaceAll("|", "\\|").replaceAll("\n", " ")} |\n`;
    }
    return s + "\n";
  };
  md += renderParams(pathParams, "Path parameters");
  md += renderParams(queryParams, "Query parameters");
  md += renderParams(headerParams, "Header parameters");

  const reqSchema = op.requestBody?.content?.["application/json"]?.schema;
  const multipart = op.requestBody?.content?.["multipart/form-data"]?.schema;
  if (reqSchema) md += `Request body: ${typeExpr(reqSchema)}\n\n`;
  else if (multipart) md += `Request body (multipart/form-data): ${typeExpr(multipart)}\n\n`;

  const responses = op.responses ?? {};
  const lines = [];
  for (const [code, res] of Object.entries(responses)) {
    if (code === "default") continue;
    const schema = res.content?.["application/json"]?.schema;
    lines.push(`- \`${code}\` — ${schema ? typeExpr(schema) : (res.description ?? "no content")}`);
  }
  lines.push(
    "- errors — [ErrorEnvelope](#errorenvelope) (domain failures) / [HTTPValidationError](#httpvalidationerror) (native 422 request-shape validation)",
  );
  md += `Responses:\n\n${lines.join("\n")}\n\n`;
  return md;
}

// --- document assembly -----------------------------------------------------------

function build() {
  let md = "";
  md += "# Acuity API reference\n\n";
  md += "**Generated** from [`packages/types/openapi.json`](../../packages/types/openapi.json) by `scripts/gen-api-docs.mjs` — do not edit by hand; re-run `pnpm run gen:api-docs` after spec changes.\n\n";
  md += `Contract version: \`${spec.info?.version ?? "?"}\`. Intentional deviations from the implemented demo backend and the recorded Phase-1 deltas are in [implementation-notes.md](implementation-notes.md); the per-endpoint backend to-do view is [endpoint-checklist.md](endpoint-checklist.md).\n\n`;
  md += "## Contract dialect\n\n";
  md += (spec.info?.description ?? "") + "\n\n";

  // group ops by first tag, preserving spec tag order
  const tagOrder = (spec.tags ?? []).map((t) => t.name);
  const tagDescriptions = Object.fromEntries((spec.tags ?? []).map((t) => [t.name, t.description ?? ""]));
  const byTag = new Map();
  let opCount = 0;
  for (const [path, item] of Object.entries(spec.paths)) {
    for (const method of METHODS) {
      const op = item[method];
      if (!op) continue;
      opCount += 1;
      const tag = op.tags?.[0] ?? "untagged";
      if (!byTag.has(tag)) byTag.set(tag, []);
      byTag.get(tag).push({ path, method, op });
    }
  }
  const orderedTags = [...tagOrder.filter((t) => byTag.has(t)), ...[...byTag.keys()].filter((t) => !tagOrder.includes(t))];

  md += "## Operations by group\n\n";
  md += "| Group | Operations | Backend status |\n|---|---|---|\n";
  for (const tag of orderedTags) {
    const ops = byTag.get(tag);
    const statuses = new Set(ops.map(({ op }) => op["x-backend-status"] ?? "EXISTS"));
    md += `| [${tag}](#${anchor("group " + tag)}) | ${ops.length} | ${[...statuses].join(", ")} |\n`;
  }
  md += `| **Total** | **${opCount}** | |\n\n`;

  for (const tag of orderedTags) {
    md += `## Group ${tag}\n\n`;
    if (tagDescriptions[tag]) md += `${tagDescriptions[tag]}\n\n`;
    for (const { path, method, op } of byTag.get(tag)) {
      md += renderOp(path, method, op);
    }
  }

  md += "## Schemas\n\n";
  for (const [name, schema] of Object.entries(schemas)) {
    md += `### ${name}\n\n`;
    if (schema.description) md += `${schema.description}\n\n`;
    md += schemaPropertiesTable(schema);
  }

  return md;
}

writeFileSync(OUT, build());
console.log(`wrote ${OUT}`);
